"""
    Add & assign:       ' += '
    Sub & assign:       ' -= '
    Multiply & assign:  ' *= '
    Divide & assign:    ' /= '
    Reminder & assign:  ' %= '
    Exponent & assign:  ' **= '
    Floor & assign:     ' //= '
    """
x = 17
y = 3

# Add & assign: " += "         'x = x + y'
x += y
print(x) # 20

# Sub & assign: " -= "         'x = x - y'
x -= y
print(x) # 17

# Multiply & assign: " *= "    'x = x * y'
x *= y
print(x) # 51

# Divide & assign: " /= "      'x = x / y'
x /= y
print(x) # 17

# Reminder & assign: " %= "    'x = x % y'
x %= y
print(x) # 2

# Exponent & assign: " **= "   'x = x ** y'
x **= y
print(x) # 8

# Floor & assign: " //= "      'x = x // y'
x //= y
print(x) # 2

