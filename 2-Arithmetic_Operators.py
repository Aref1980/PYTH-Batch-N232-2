"""
    Addition:       ' + '
    Subtraction:    ' - '
    Divide:         ' / '
    Multiplication: ' * '
    Reminder:       ' % '
    Exponent:       ' ** '
    Floor Division: ' // '
"""

num1 = 13
num2 = 5

# Addition: " + "
sum = num1 + num2
print(sum) #18

sum1 = 9 + 8
print(sum1) #17

# Subtraction: " - "

sub = num1 - num2
print(sub) # 8

sub1 = 18 - 6
print(sub1) #12

# Divide: " / "
div = num1 / num2
print(div) # 2.6

div1 = 20 / 5
print(div1) # 4.0

# Multiplication: " * "
mul = num1 * num2
print(mul) # 65

mul_1 = 4 * 6
print(mul_1) # 24

# Reminder: " % "
rem = num1 % num2
print(rem) # 3

rem1 = 13 % 4
print(rem1) # 1

# Exponent: " ** "
exp = 5 ** 2
print(exp) # 25

exp1 = 5 ** 3
print(exp1) # 125

exp2 = 2 ** 4
print(exp2) # 16

# Floor Division: " // "
"""Floor Division represent divide result that value always integer value or nearest smallest integer value"""
fld = num1 // num2 # 13 / 5 = 2.6
print(fld) # 2

fld1 = 15 // 4 # 15 / 4 = 3.75
print(fld1) # 3

fld2 = -15 // 4 # -15 / 4 = -3.75
print(fld2) # -4

