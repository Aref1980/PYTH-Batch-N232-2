# Comparison Operators execution result: True/False

num1 = 10
num2 = 12

# Equal: " == "
print(num1 == num2) # False

# Not Equal: " != "
print(num1 != num2) #True

# less than: " < "
print(num1 < num2) #True

# less than Equeal: " <= "
print(num1 <= num2) #True

# Greater than: " > "
print(num1 > num2) # False

# Greater than Equeal: " >= "
print(num1 >= num2) # False


